package moduletwo;

public class Task3 {
    public static void main(String[] args) {

        int a = 4; // Сторона прямоугольника a //
        int b = 2; // Сторона прямоугольника b //
        int s = a * b; // Площадь прямоугольника //
        int p = 2 * (a + b); // Периметр прямоугольника //
        System.out.println("Площадь прямоугольника = " + s);
        System.out.println("Периметр прямоугольника = " + p);

    }
}
